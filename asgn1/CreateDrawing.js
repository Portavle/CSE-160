function createDrawing() {
    console.log("draw");
    for(var i = 0; i < imageData.length; i++) {
        var newPoint;
        if(imageData[i][0] == "tri") {
            newPoint = new Triangle();

            newPoint.position = [imageData[i][1][0] / 8 - 1, imageData[i][1][1] / 8 - 0.8];
            newPoint.color = [imageData[i][4][0], imageData[i][4][1], imageData[i][4][2], imageData[i][4][3]];
            newPoint.size = 1;
            newPoint.vertices = [imageData[i][1][0] / 8 - 1, imageData[i][1][1] / 8 - 0.8, 
                imageData[i][2][0] / 8 - 1, imageData[i][2][1] / 8 - 0.8, 
                imageData[i][3][0] / 8 - 1, imageData[i][3][1] / 8 - 0.8]
        }
        if(imageData[i][0] == "circle") {
            newPoint = new Circle();

            newPoint.position = [imageData[i][1][0] / 8 - 1, imageData[i][1][1] / 8 - 0.8];
            newPoint.color = [imageData[i][4][0], imageData[i][4][1], imageData[i][4][2], imageData[i][4][3]];
            newPoint.size = imageData[i][2] * 24;
            newPoint.segments = imageData[i][3];
        }
        g_shapesList.push(newPoint);
    }
    console.log(g_shapesList);
    renderAllShapes();
}